# Task 1: Grocery Store Math
# Define the prices of each item
price_bread = 2.50
price_peanut_butter = 3.75
price_jelly = 1.99

# Calculate the total cost
total_cost = price_bread + price_peanut_butter + price_jelly

# Print the total cost
print(f"The total cost of bread, peanut butter and jelly is: ${total_cost:.2f}")

# Task 2: Bank Interest
# Initial amount in the savings account
initial_amount = 10000

# Fixed yearly interest rate (converted to decimal)
interest_rate = 0.07

# Calculate the amount at the end of the year
amount_after_year = initial_amount * (1 + interest_rate)

# Print the amount after one year
print(f"The amount in the account after one year will be: ${amount_after_year:.2f}")